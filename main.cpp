BLEService testService(BLE_UUID_TEST_SERVICE);
BLEStringCharacteristic fileNameCharacteristic(BLE_UUID_FILE_NAME, BLERead | BLEWrite, 40);
String fileName = "";
String temperatuuur()
{
  float temp = bme.readTemperature();
  String tempe;
  tempe.concat(temp);
  String Sensoren = ("de temperatuur is: " + tempe);
  return Sensoren;
}

void setup()
{
  // set up the LCD's number of columns and rows:
  Serial.begin(9600);
  pinMode(3, OUTPUT);//buzzer
  while (!Serial)
    ; // time to get serial running
  Serial.println(F("BME280 test"));

  unsigned status;

  // default settings
  status = bme.begin(0x76);
  // You can also pass in a Wire library object like &Wire2
  // status = bme.begin(0x76, &Wire2)
  if (!status)
  {
    Serial.println("Could not find a valid BME280 sensor, check wiring, address, sensor ID!");
    Serial.print("SensorID was: 0x");
    Serial.println(bme.sensorID(), 16);
    Serial.print("        ID of 0xFF probably means a bad address, a BMP 180 or BMP 085\n");
    Serial.print("   ID of 0x56-0x58 represents a BMP 280,\n");
    Serial.print("        ID of 0x60 represents a BME 280.\n");
    Serial.print("        ID of 0x61 represents a BME 680.\n");
    while (1)
      delay(10);
  }

  Serial.println("-- Default Test --");


  Serial.println();

  lcd.begin(16, 2);
  // Print a message to the LCD.
  while (!Serial)
    ;

  BLE.begin();

  // set advertised local name and service UUID:
  BLE.setDeviceName("Arduino Nano 33 BLE");
  BLE.setLocalName("Arduino Nano 33 BLE");
  BLE.setAdvertisedService(testService);

  // BLE add characteristics
  testService.addCharacteristic(fileNameCharacteristic);

  // add service
  BLE.addService(testService);

  // set the initial value for the characeristic:
  fileNameCharacteristic.writeValue(fileName);

  // start advertising
  BLE.advertise();
}

void loop()
{
  BLEDevice central = BLE.central();

  if (central)
  {
    Serial.print("Connected to central: ");
    Serial.println(central.address());
    while (central.connected())
    {
      if (fileNameCharacteristic.written())
      {
        lcd.clear();
        fileName = fileNameCharacteristic.value();
        int ja = fileName.length();
        fileNameCharacteristic.writeValue(temperatuuur());
        if (ja <= 16)
        {
          lcd.print(fileName);
          
        }
        if (ja >= 17)
        {
          String een = fileName.substring(0, 16);
          String twee = fileName.substring(16);
          lcd.print(een);
          lcd.setCursor(0, 1);
          lcd.print(twee);
        }
        if(fileName=="super mario"){
        sing(1);
        sing(1);
        sing(2);
        }
        
      }
    }

    Serial.print(F("Disconnected from central: "));
    Serial.println(central.address());
  }
}
